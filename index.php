    La domotique, qu’est-ce que c’est ?<br/>
Le terme « Domotique », dérivé du latin Domus (la maison) est une contraction de domus et informatique.
<br/>
La domotique regroupe l’ensemble des techniques et technologies électroniques, informatiques et des moyens de télécommunications permettant d’automatiser et d’améliorer les tâches au sein d’une maison.
<br/>
Ces taches peuvent être aussi bien liées au confort (lumières, automatisation des tâches ménagères…), aux économies d’énergie (gestion du chauffage, analyse de la consommation..) ou à la sécurité (détecteur de fumés, alarmes, vidéosurveillance…).
<br/>
Grâce à des capteurs intégrés au coeur de la maison, celle-ci devient communicante.<br/>
<br/>
Cela permet de connaître son état à tout moment :<br/>

les lumières qui sont allumées
la température dans chaque pièce
l’état des ouvertures (portes, fenêtres…)
les pièces occupées
<br/>

A partir de ces états, il est possible d’agir sur des modules de commande :<br/>
<br/>
marche/arrêt d’une lumière
marche/arrêt du chauffage
marche/arrêt d’un appareil électrique
<br/>

Chaque action pourra aussi être contrôlée de manière automatique, par exemple à horaire fixe, ou avec des modules de commande (télécommandes, écrans tactiles…)
<br/>
C'est pour cela qu'un groupe de technicien du lycée Jules Ferry  a été choisi pour simuler une domotique géneral pour plus d'explication je vous invite à regarder l'onglet presenation.
